<script>
export default {
    name: 'TaskItem',
    props: {
        task: Object
    },
    methods: {
        onDelete(id) {
            this.$emit('delete-task', id)
        }
    }
}
</script>

<template>
    <div @dblclick="$emit('toggle-reminder', task.id)" :class="[task.reminder ? 'reminder' : '', 'task']">
        <h3>
            {{ task.text }}
            <i @click="onDelete(task.id)" class="fas fa-times" />
        </h3>
        <p>{{ task.day }}</p>
    </div>
</template>

<style scope>
.fas {
  color: red;
}
.task {
  background: #f4f4f4;
  margin: 5px 0 5px 5px;
  padding: 10px 20px;
  cursor: pointer;
}
.task.reminder {
